// 4) Dado que x = 10 e y = 5:
// a) mostre a soma,
// b) subtração,
// c) multiplicação,
// d) divisão e resto entre elas.



let x = 10;
let y = 5;

console.log("a) Soma: " + (x + y));
console.log("b) Subtração: " + (x - y));
console.log("c) Multiplicação: " + (x * y));
console.log("d) Divisão: " + (x / y));
console.log("e) Resto da divisão: " + (x % y));